#include <stdio.h>

int		ft_sqrt(int nb)
{
	long int i;

	i = 1;
	if (nb == 0)
		return (0);
	while ((i * i) < nb && i < 46341)
		i++;
	if ((i * i) == nb)
		return (i);
	else
		return (0);
}
