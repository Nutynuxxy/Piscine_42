#include <unistd.h>

int g_a;
int g_b;
int g_c;

void	ft_print_comb(void)
{
	g_a = '0';
	while (g_a <= '7')
	{
		g_b = g_a + 1;
		while (g_b <= '8')
		{
			g_c = g_b + 1;
			while (g_c <= '9')
			{
				write(1, &g_a, 1);
				write(1, &g_b, 1);
				write(1, &g_c, 1);
				{
					if (g_a < '7' && g_b <= '8' && g_c <= '9')
					{
						write(1, ", ", 2);
					}
				}
				g_c++;
			}
			g_b++;
		}
		g_a++;
	}
}
